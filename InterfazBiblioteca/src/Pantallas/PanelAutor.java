    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Se agrega o modifica registro según botón seleccionado.
        try{            
            String nom = txtNombre.getText();
            String apeP = txtApellidoP.getText();
            String apeM = txtApellidoM.getText();

            if(btnAgregar.isSelected()){
                BDAutor.insertar(nom, apeP, apeM);
                BDAutor.listar(tableTablaDatos);
                Limpiar.contenidoPanel(panelCampos);
                comprobarCampos();
            }else if(btnModificar.isSelected()){
                int id = Integer.parseInt(lblID.getText());
                BDAutor.modificar(id, nom, apeP, apeM);
                BDAutor.listar(tableTablaDatos);
                Limpiar.contenidoPanel(panelCampos);
                comprobarCampos();
            }
        }catch(Exception e){
            System.err.println("Excepción al guardar : " + e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

