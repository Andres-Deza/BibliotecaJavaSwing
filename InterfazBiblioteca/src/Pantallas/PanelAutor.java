    /**
     * Método para comprobar los campos, habilitar y deshabilitar botones de acción
     */
    public void comprobarCampos(){
        String cc = txtNombre.getText() + txtApellidoP.getText() + txtApellidoM.getText();
        if ( cc.equals("")){
            btnGuardar.setEnabled(false);
            btnLimpiar.setEnabled(false);            
        }else if (txtNombre.getText().isEmpty() || txtApellidoP.getText().isEmpty() || txtApellidoM.getText().isEmpty()) {
            btnGuardar.setEnabled(false);
            btnLimpiar.setEnabled(true);
        }else{
            btnGuardar.setEnabled(true);
            btnLimpiar.setEnabled(true);
        }
        if (tableTablaDatos.getSelectedRow() == -1){
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }else{
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }
        
    public void obtenerCampos(){
        int rowSel = tableTablaDatos.getSelectedRow();
        lblID.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 0)));
        txtNombre.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 1)));
        txtApellidoP.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 2)));
        txtApellidoM.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 3)));
    }
    
