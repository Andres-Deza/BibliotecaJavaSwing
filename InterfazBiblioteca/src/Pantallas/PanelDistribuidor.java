    /**
     * Método para habilitar y deshabilitar botones de acción
     */
    public void comprobarCampos(){
        String cc = txtNumRut.getText() + txtDigRut.getText() + txtNombre.getText() + txtDireccion.getText() + txtTelefono.getText();
        if ( cc.equals("")){
            btnGuardar.setEnabled(false);
            btnLimpiar.setEnabled(false);            
        }else if (txtNumRut.getText().isEmpty() || txtDigRut.getText().isEmpty() || txtNombre.getText().isEmpty() || 
                txtDireccion.getText().isEmpty() || txtTelefono.getText().isEmpty()) {
            btnGuardar.setEnabled(false);
            btnLimpiar.setEnabled(true);
        }else{
            btnGuardar.setEnabled(true);
            btnLimpiar.setEnabled(true);
        }
        
        //Se activan o desactivan botones
        if (tableTablaDatos.getSelectedRow() == -1){
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }else{
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }
    
    /**
     * Método para obtener los campos del item seleccionado en la tabla principal
     */
    public void obtenerCampos(){
        //Se obtienen datos de fila seleccionada en la tabla y se cargan los campos correspondientes
        int rowSel = tableTablaDatos.getSelectedRow();
        lblID.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 0)));
        String rut = String.valueOf(tableTablaDatos.getValueAt(rowSel, 1));
        String numRut = rut.substring(0, rut.length()-2);
        String digRut = rut.substring(rut.length()-1);
        txtNumRut.setText(numRut);
        txtDigRut.setText(digRut);
        txtNombre.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 2)));
        txtDireccion.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 3)));
        txtTelefono.setText(String.valueOf(tableTablaDatos.getValueAt(rowSel, 4)));
        jYearAnio.setValue((Integer) tableTablaDatos.getValueAt(rowSel, 5));
    }
    
