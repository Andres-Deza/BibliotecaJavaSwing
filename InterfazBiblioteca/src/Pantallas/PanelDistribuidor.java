    private void txtNumRutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumRutKeyTyped
        int limite = 8;
        if ( txtNumRut.getText().length() >= limite){
            evt.consume();
        }
        char enter = evt.getKeyChar();
        if ( !(Character.isDigit(enter))){
            evt.consume();
        }        
    }//GEN-LAST:event_txtNumRutKeyTyped

    private void txtDigRutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDigRutKeyTyped
        //Limite de campo de text
        int limite = 1;
        if ( txtDigRut.getText().length() >= limite){
            evt.consume();
        }
        //Solo se acepta numero o caracter "k"
        char enter = evt.getKeyChar();
        if ( !(Character.isDigit(enter)) && !String.valueOf(enter).toLowerCase().equals("k")){
            evt.consume();
        }
    }//GEN-LAST:event_txtDigRutKeyTyped

